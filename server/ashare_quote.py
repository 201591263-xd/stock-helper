# ========== 腾讯行情 ==========

def _get_price_day_tx(code, end_date='', count=10, frequency='1d'):
    unit = 'week' if frequency in '1w' else 'month' if frequency in '1M' else 'day'
    if end_date:
        end_date = end_date.strftime('%Y-%m-%d') if isinstance(end_date, datetime.date) else end_date.split(' ')[0]
    end_date = '' if end_date == datetime.datetime.now().strftime('%Y-%m-%d') else end_date
    URL = f'http://web.ifzq.gtimg.cn/appstock/app/fqkline/get?param={code},{unit},,{end_date},{count},qfq'
    st = json.loads(requests.get(URL, timeout=5).content)
    ms = 'qfq' + unit
    stk = st['data'][code]
    buf = stk[ms] if ms in stk else stk[unit]
    df = pd.DataFrame(buf, columns=['time', 'open', 'close', 'high', 'low', 'volume'], dtype='float')
    df.time = pd.to_datetime(df.time)
    df.set_index(['time'], inplace=True)
    df.index.name = ''
    return df

# ========== 新浪行情 ==========

def _get_price_sina(code, end_date='', count=10, frequency='60m'):
    frequency = frequency.replace('1d', '240m').replace('1w', '1200m').replace('1M', '7200m')
    ts = int(frequency[:-1]) if frequency[:-1].isdigit() else 1
    URL = f'http://money.finance.sina.com.cn/quotes_service/api/json_v2.php/CN_MarketData.getKLineData?symbol={code}&scale={ts}&ma=no&datalen={count}'
    try:
        st = json.loads(requests.get(URL, timeout=5).content)
        if not st:
            return None
        df = pd.DataFrame(st)
        df = df.rename(columns={'day': 'time', 'volume': 'volume'})
        for col in ['open', 'close', 'high', 'low', 'volume']:
            if col in df.columns:
                df[col] = df[col].astype('float')
        df.time = pd.to_datetime(df.time)
        df.set_index(['time'], inplace=True)
        df.index.name = ''
        return df[['open', 'close', 'high', 'low', 'volume']] if all(c in df.columns for c in ['open', 'close', 'high', 'low', 'volume']) else df
    except:
        return None

# ========== 实时行情（腾讯分钟线） ==========

def _get_realtime_tx(code):
    """获取实时价格"""
    try:
        URL = f'http://qt.gtimg.cn/q={code}'
        text = requests.get(URL, timeout=3).content.decode('gbk', errors='replace')
        # 解析
        for line in text.strip().split('\n'):
            if '=' not in line:
                continue
            data = line.split('="')[1].rstrip('";')
            fields = data.split('~')
            if len(fields) < 35:
                continue
            return {
                'name': fields[1],
                'code': fields[2],
                'price': float(fields[3]) if fields[3] else 0,
                'change_pct': float(fields[32]) if fields[32] else 0,
                'high': float(fields[33]) if fields[33] else 0,
                'low': float(fields[34]) if fields[34] else 0,
            }
    except:
        pass
    return None

# ========== 统一接口 ==========

def get_stock_prefix(code: str) -> str:
    """补全行情前缀"""
    if code.startswith('6'):
        return f'sh{code}'
    return f'sz{code}'

def get_realtime_quote(code: str) -> dict:
    """获取单只股票实时行情 (双核自动切换)"""
    prefix = get_stock_prefix(code)
    
    # 先试腾讯
    result = _get_realtime_tx(prefix)
    if result:
        return result
    
    # 兜底：用新浪日线最近收盘价
    try:
        df = _get_price_sina(prefix, count=1)
        if df is not None and len(df) > 0:
            return {
                'name': code,
                'code': code,
                'price': float(df['close'].iloc[-1]),
                'change_pct': 0,
                'high': float(df['high'].iloc[-1]),
                'low': float(df['low'].iloc[-1]),
            }
    except:
        pass
    
    return {'name': code, 'code': code, 'price': 0, 'change_pct': 0, 'high': 0, 'low': 0}

def get_batch_quotes(codes: list) -> dict:
    """批量获取实时行情"""
    results = {}
    for code in codes:
        q = get_realtime_quote(code)
        if q:
            results[code] = q
    return results

