def get_cyb_stocks_fallback():
    """
    兜底方案：用腾讯 API 批量获取快照中创业板股票的行情
    当东方财富接口不可用时使用
    """
    try:
        codes = [c for c, _, _ in _CYB_INDUSTRY_SNAPSHOT]
        quotes = get_batch_quotes(codes)

        stocks = []
        for code, name, industry in _CYB_INDUSTRY_SNAPSHOT:
            q = quotes.get(code, {})
            price = q.get('price', 0) or 0
            change_pct = q.get('change_pct', 0) or 0
            if price > 0:
                stocks.append({
                    'code': code,
                    'name': name,
                    'price': price,
                    'change_pct': change_pct,
                    'industry': industry,
                    'main_net': 0,
                    'mcap': 0,
                })

        _cyb_cache = {'data': stocks, 'ts': time.time()}
        print(f"[行情-兜底] 创业板: {len(stocks)} 只 (腾讯批量)")
        return stocks
    except Exception as e:
        print(f"[行情-兜底] 失败: {e}")
        return _cyb_cache['data']


def build_dynamic_pool(board_filter=''):
    """
    从全市场创业板动态构建板块→股票池
    board_filter: ''=全市场 / '创业板'=仅30xxxx / '主板'=仅60xxxx+00xxxx / '科创板'=仅68xxxx

    返回: {板块名: [(code, name, reason), ...]}
    """
    stocks = get_all_cyb_stocks()

    # 按板块过滤代码前缀
    if board_filter == '创业板':
        stocks = [s for s in stocks if s['code'].startswith('30')]
    elif board_filter == '主板':
        stocks = [s for s in stocks if s['code'].startswith(('60', '00'))]
    elif board_filter == '科创板':
        stocks = [s for s in stocks if s['code'].startswith('68')]
    # 未指定时默认取全量，包含所有 board

    # 按行业归类到板块
    pool = defaultdict(list)
    for s in stocks:
        sector = _INDUSTRY_SECTOR_MAP.get(s['industry'])
        if not sector:
            continue  # 跳过没有映射行业的股票
        reason = f"{s['industry']} 涨跌幅{s['change_pct']:+.2f}%"
        pool[sector].append((s['code'], s['name'], s['change_pct'], s['price'], reason))

    # 每个板块按涨跌幅排序，取前15只
    result = {}
    for sector, candidates in pool.items():
        candidates.sort(key=lambda x: x[2], reverse=True)
        # 去掉 change_pct 和 price，恢复为 (code, name, reason) 三元组
        result[sector] = [(c, n, r) for c, n, _, _, r in candidates[:15]]

    print(f"[动态池] 板块数={len(result)} 总候选={sum(len(v) for v in result.values())}")
    return result


